        <p>Для <span class="highlight">разработчиков</span> мы создали отдельную систему управления студией, проектами и сотрудниками. Также вам доступны медиа-площадки, на которых вы можете публиковать новости, анонсы своих проектов и общаться с игроками.</p>

        <p>Для <span class="highlight">игроков</span> мы предлагаем библиотеку качественного контента, удобную систему рекомендаций и персональные коллекции. Студии и игры проходят модерацию, поэтому ваш игровой опыт будет потрясающий: откройте для себя мир инди-игр!</p>

        <h2>Технологии</h2>

        <p>Мы хотим принести как можно больше удобных фич и новшеств в игровую индустрию. Вот что мы уже реализовали/реализуем:</p>

        <ul>
            <li>Программа DuStudio для более быстрой и удобной загрузки игр</li>
            <li>Свои алгоритмы рекомендаций</li>
            <li>Персональный бот-помощник</li>
            <li>Безлимитное хранилище для ваших проектов</li>
            <li>Лаунчер для игроков</li>
            <li>API для разработчиков</li>
            <li>Система оплаты (разработчики могут монетизировать свой проект)</li>
            <li>Система подписок для игроков и разработчиков</li>
        </ul>

